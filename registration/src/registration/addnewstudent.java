public void fetch() {
    
    try {
        
        ver();
        
        String sql = "Select ID, StudentName, Address, ContactNo, Course from stud ORDER BY ID ASC" ;
        PreparedStatement pst = cn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        pst.close();
        
    }
    catch (Exception e) {
        
    }
}
public void table() {
     try {
        
        ver();
        
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        int row = jTable1.getSelectedRow();
        String value = (jTable1.getModel().getValueAt(row, 0).toString());
        
        String Gsql = "SELECT * FROM stud where ID =" + value ;
        PreparedStatement pst = cn.prepareStatement(Gsql);
        ResultSet rs = pst.executeQuery(); 
       
        if(rs.next()) {
             tfSId.setText(model.getValueAt(i,0).toString());
             tfSName.setText(model.getValueAt(i,1).toString());
             tfSAdd.setText(model.getValueAt(i,2).toString());
             tfSCont.setText(model.getValueAt(i,3).toString());
             tfSCour.setText(model.getValueAt(i,4).toString());
        }
        
    }
    catch (Exception e) {
        
    }
}
