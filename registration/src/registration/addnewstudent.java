 public void addStud() {
     try {
         ver();
         
         String sql = "Insert into stud"
                + "(StudentName,Address,ContactNo,Course)"
                + "values (?,?,?,?)";
        PreparedStatement pst = cn.prepareStatement(sql);
        
        pst.setString(1, tfSName.getText());
        pst.setString(2, tfSAdd.getText());
        pst.setString(3, tfSCont.getText());
        pst.setString(4, tfSCour.getText());
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null,"Student Added...");
        fetch();
        tfSName.setText("");
        tfSAdd.setText("");
        tfSCont.setText("");
        tfSCour.setText("");
        pst.close();
        
     }
     catch (Exception e) {
         JOptionPane.showMessageDialog(this, e.getMessage());
     }
 }
 public void delStud() {
     try {
         ver();
         
          String value = tfSId.getText();            
        
            String delsql = "DELETE FROM stud where ID =" + value ;
            PreparedStatement pst = cn.prepareStatement(delsql);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Deleted!");
            fetch();
            tfSId.setText("");
            tfSName.setText("");
            tfSAdd.setText("");
            tfSCont.setText("");
            tfSCour.setText("");
            pst.close();
    
     }
     catch (Exception e) {
         
     }
 }
 public void upStud() {
     try {
         
        ver();
        String value = (tfSId.getText());
        String sql = "UPDATE stud SET StudentName = ?, Address = ?, ContactNo = ?, Course = ? where ID = "+ value;
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, tfSName.getText());
        pst.setString(2, tfSAdd.getText());
        pst.setString(3, tfSCont.getText());
        pst.setString(4, tfSCour.getText());
        pst.execute();
        JOptionPane.showMessageDialog(this, "Updated Successfully!");                                       
        fetch(); 
        tfSId.setText("");
        tfSName.setText("");
        tfSAdd.setText("");
        tfSCont.setText("");
        tfSCour.setText("");
        pst.close();
         
     }
     catch (Exception e) {
         
     }
 }
 
