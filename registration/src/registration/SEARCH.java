public void fetch() {
    
    try {
        
        ver();
        
        String sql = "Select book_id, author, title, shelf from book ORDER BY book_id ASC" ;
        PreparedStatement pst = cn.prepareStatement(sql);
        ResultSet rs = pst.executeQuery();
        jTable1.setModel(DbUtils.resultSetToTableModel(rs));
        pst.close();
        
    }
    catch (Exception e) {
        
    }
}
public void delBook() {
    try {
        
        ver();
        
            String value = tfBId.getText();            
        
            String delsql = "DELETE FROM book where book_id =" + value ;
            PreparedStatement pst = cn.prepareStatement(delsql);
            pst.executeUpdate();
            
            JOptionPane.showMessageDialog(this, "Deleted!");
            fetch();
            tfBId.setText("");
            tfBAuth.setText("");
            tfBTit.setText("");
            tfBShelf.setText("");
            pst.close();
    }
    catch (Exception e) {
        
    }
}
public void upBook() {
    try {
        
        ver();
        String value = (tfBId.getText());
        String sql = "UPDATE book SET author = ?, title = ?, shelf = ? where book_id = "+ value;
        PreparedStatement pst = cn.prepareStatement(sql);
        pst.setString(1, tfBAuth.getText());
        pst.setString(2, tfBTit.getText());
        pst.setString(3, tfBShelf.getText());
        pst.execute();
        JOptionPane.showMessageDialog(this, "Updated Successfully!");                                       
        fetch(); 
        tfBId.setText("");
        tfBAuth.setText("");
        tfBTit.setText("");
        tfBShelf.setText("");
        pst.close();
    }
    catch (Exception e) {
        JOptionPane.showMessageDialog(this,e.getMessage());
    }
}
public void table() {
    try {
        
        ver();
        
        int i = jTable1.getSelectedRow();
        TableModel model = jTable1.getModel();
        int row = jTable1.getSelectedRow();
        String value = (jTable1.getModel().getValueAt(row, 0).toString());
        
        String Gsql = "SELECT * FROM book where book_id =" + value ;
        PreparedStatement pst = cn.prepareStatement(Gsql);
        ResultSet rs = pst.executeQuery(); 
       
        if(rs.next()) {
             tfBId.setText(model.getValueAt(i,0).toString());
             tfBAuth.setText(model.getValueAt(i,1).toString());
             tfBTit.setText(model.getValueAt(i,2).toString());
             tfBShelf.setText(model.getValueAt(i,3).toString());
        }
        
    }
    catch (Exception e) {
        
    }
}

