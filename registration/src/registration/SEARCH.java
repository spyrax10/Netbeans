public void addBook() {
    
    
    try {
        
        ver();
        
        String sql = "Insert into book"
                + "(author,title,shelf)"
                + "values (?,?,?)";
        PreparedStatement pst = cn.prepareStatement(sql);
        
        pst.setString(1, tfBAuth.getText());
        pst.setString(2, tfBTit.getText());
        pst.setString(3, tfBShelf.getText());
        pst.executeUpdate();
        JOptionPane.showMessageDialog(null,"Book Added...");
        fetch();
        tfBId.setText("");
        tfBAuth.setText("");
        tfBTit.setText("");
        tfBShelf.setText("");
        pst.close();
        
    }
     catch (Exception e) {
         
     }
}
 public void search() {
         try {
             
             ver();
             
                String rsql = "SELECT * FROM book WHERE book_id=?"; 
                PreparedStatement pst = cn.prepareStatement(rsql);
                pst.setString(1,tfSer.getText());
                ResultSet rs = pst.executeQuery(); 
                jTable1.setModel(DbUtils.resultSetToTableModel(rs));
             
         }
         catch (Exception e) {
             JOptionPane.showMessageDialog(this, e.getMessage());
         }
     }
