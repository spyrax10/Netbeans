private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String username = tfRUser.getText();
        String password = tfRPass.getText();
        String Repass = tfRType.getText();
        
        if (!password.equals(Repass)) {
            JOptionPane.showMessageDialog(null, "Password Don't Match");
            tfRPass.setText("");
            tfRType.setText("");
            tfRPass.grabFocus();
        }
        if (username.isEmpty() || password.isEmpty() || Repass.isEmpty()) {
            JOptionPane.showMessageDialog(null, "Input missing Fields");
        }
        else{
            chkUsername();
}
}//GEN-LAST:event_jButton1ActionPerformed

