public void addUser() {
    
    try {
        
        ver();
        
        String sql = "Insert into usertab"
                + "(studID,username,password)"
                + "values (?,?,?)";
        PreparedStatement pst = cn.prepareStatement(sql);
         
         pst.setString(1, tfId.getText());
         pst.setString(2, tfRUser.getText());
         pst.setString(3, tfRPass.getText());
         pst.executeUpdate();
         JOptionPane.showMessageDialog(null,"Success!!");
         pst.close();
         close();
         LOGIN l = new LOGIN();
         l.show();
         
    }
    catch (Exception e) {
        
    }
}
public void chkUsername() {
    try {
        
        ver();
        
        String sql = "SELECT * FROM usertab WHERE username=?";
        PreparedStatement pst = cn.prepareStatement(sql);
        
        pst.setString(1,tfRUser.getText());
        ResultSet rs = pst.executeQuery();
        
        if (rs.next()) {
            JOptionPane.showMessageDialog(null,"Username already Taken.");
            tfRPass.setText("");
            tfRType.setText("");
            tfRUser.grabFocus();
        }
        else {
            addUser();
        }
        
    }
    catch (Exception e) {
    }
}
        

