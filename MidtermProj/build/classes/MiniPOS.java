    private void tfCashKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCashKeyTyped
        
    
        char c = evt.getKeyChar();
        
        if (c == KeyEvent.VK_ENTER)
        
        {
            if (tfCash.getText().equals(""))
            
                {
                   JOptionPane.showMessageDialog(null ,"Please Insert Some Cash.." , "ERROR!" , JOptionPane.INFORMATION_MESSAGE);
                    tfCash.setToolTipText("Please Insert Some Cash..");
                    tfCash.grabFocus();
            
                }
            
                String cash = tfCash.getText();
                String tot = tfTotal.getText();
                double Rcash = Double.parseDouble(cash);
                double Rtot = Double.parseDouble(tot);
            
                if (Rcash < Rtot)
            
                {
                
                    JOptionPane.showMessageDialog(null,"Insufficient Cash..");
                 
                    tfCash.setText("");
                 
                    tfCash.grabFocus();
            
                }
            
                else
            
                {
              
                    Stot = Double.parseDouble(cash) - Double.parseDouble(tot);
              
                    String RStot = String.format ("%.2f" , Stot);
              
                    tfChange.setText(RStot);
                    receipt();
               JOptionPane.showMessageDialog(null ,"Transaction Completed!!" , "SpyraxApp, Inc." , JOptionPane.INFORMATION_MESSAGE);
                    print.show();
                    Clear();
                    buttonIB.grabFocus();
                }
        
        }
        
       
        
    }//GEN-LAST:event_tfCashKeyTyped

    private void tfCashKeyPressed(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_tfCashKeyPressed
        
      char c = evt.getKeyChar();
        if(!(((c >= '0') && (c <= '9'))
                || (c == KeyEvent.VK_BACKSPACE)
                || (c == KeyEvent.VK_DELETE)
                || (c == KeyEvent.VK_ENTER)
                || (c == KeyEvent.VK_PERIOD))) {
            
             getToolkit().beep();
             JOptionPane.showMessageDialog(null ,"Numbers only!" , "ERROR!" , JOptionPane.INFORMATION_MESSAGE);
             tfCash.setText("");
             tfCash.grabFocus();
             
             evt.consume();
        }   
    }//GEN-LAST:event_tfCashKeyPressed

