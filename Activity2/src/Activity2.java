    private void eqlActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_eqlActionPerformed
        
         if (show.getText() != null && show.getText().equals(""))
        {
         
             JOptionPane.showMessageDialog(null ,"Input Number First!!" , "ERROR!" , JOptionPane.INFORMATION_MESSAGE);
             Calculator.show();
        }    
        
        else
         {
        
        String answer;
        secondnum = Double.parseDouble(show.getText());
        if(operations == "+")
        {
            result = firstnum + secondnum;
            answer = String.format("%.2f",result);
            show.setText(answer);
        }
          else  if(operations == "-")
        {
            result = firstnum - secondnum;
            answer = String.format("%.2f",result);
            show.setText(answer);
        }
           else  if(operations == "*")
        {
            result = firstnum * secondnum;
            answer = String.format("%.2f",result);
            show.setText(answer);
        }
           else  if(operations == "/")
        {
            result = firstnum / secondnum;
            answer = String.format("%.2f",result);
            show.setText(answer);
        }
           else  if(operations == "%")
        {
            result = firstnum % secondnum;
            answer = String.format("%.2f",result);
            show.setText(answer);
        }
        else
           {
               show.setText("Syntax Error!!");
           }
        
        
         }
        
    }//GEN-LAST:event_eqlActionPerformed

