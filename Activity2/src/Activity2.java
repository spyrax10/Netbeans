    private void gerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gerActionPerformed
       
        if (table.getText().trim().isEmpty()== false)
        {
             JOptionPane.showMessageDialog(null ,"Clear text first!" , "ERROR!" , JOptionPane.INFORMATION_MESSAGE);
        }
        if (txt.getText().trim().isEmpty())
        {
             JOptionPane.showMessageDialog(null ,"Input a number!" , "ERROR!" , JOptionPane.INFORMATION_MESSAGE);
        }
        else
        {
        
        int b , i , m;
        b = Integer.parseInt (txt.getText());
        
        for ( i = 1 ; i <= 10 ; i++)
        {
            m = b * i ;
            table.append ( b + " * " + i + " = " + m + "\n");
        }
        txt.setText("");
        
        }
    }//GEN-LAST:event_gerActionPerformed

